#Dadas las horas trabajadas de una persona y su tarifa por hora, calcular y mostrar su salario.#

Working_hours = int(input('Ingrese las horas trabajadas este mes: '))
Per_hour = int(input('Ingrese la tarifa por hora en USD: '))

Salaria_Mensual = Working_hours * Per_hour

print('Su salario este mes es de: ',Salaria_Mensual)